 /**
     * De acuerdo al indice de la pregunta establece en la interfaz grafica la 
     * pregunta actual
     */   
      private void setUI(){
        jTema.setText(this.objList.get(indexQuestion).getTema());
        ETISubtema.setText(this.objList.get(indexQuestion).getSubTema());
        jNum.setText(this.objList.get(indexQuestion).getNum());
        ETIPregunta.setText(this.objList.get(indexQuestion).getPregunta());
        jUnidad.setText(this.objList.get(indexQuestion).getUnidad());
        jR1.setText(this.objList.get(indexQuestion).getRes1());
        jR2.setText(this.objList.get(indexQuestion).getRes2());
        jR3.setText(this.objList.get(indexQuestion).getRes3()); 
    }
        /**
     * Obtiene un numero aleatorio el cual sera el indice de la siguiente pregunta
     * 
     */
       private int getNext(){
        Random r = new Random();
        int High = this.objList.size();
        int Result = r.nextInt(High-0);
        return Result;
    }
        /**
     * 
     * evalua si la respuesta de la pregunta es correcta
     */
       private void isCorrect(javax.swing.JRadioButton objRadio)
    {
        String answer = this.objList.get(this.indexQuestion).getRes();
        String yourAnswer = objRadio.getText();
        if(answer.equals(yourAnswer)){
            this.puntaje++;
             }   
        this.Respuestas.clearSelection();
        this.objList.remove(this.indexQuestion);
        
        if(this.objList.size()!=0){
            this.indexQuestion = this.getNext(); 
            this.setUI();  
        }else{
            ///TODO monstrar que pex porque se acabaron las preguntas
             dispose();
             
            Resume jResume = new Resume();
            jResume.score = this.puntaje;
            jResume.countQuestion = this.totalQuestion;
            jResume.setData();
            jResume.setVisible(true);
        }
       
    }
