/**
     * Este metodo lo que hace es recorrer todas las preguntas guardadas en el archivo de objetos preguntas.dat
     * y lo añade a una lista del tipo de preguntas para que sean procesadas 
     */
    private void setQuestions(){
    try { 
            PreguntasRandom.crearFilePreguntas(new java.io.File ("preguntas.dat"));            
            for(int i=0; i<getNumeroRegistros(); i++) {
                flujo.seek(i * PreguntasRandom.tamañoRegistro);             
                objList.add(
                    new Preguntas(
                            flujo.readUTF(),
                            flujo.readUTF(),
                            flujo.readUTF(),
                            flujo.readUTF(),
                            flujo.readUTF(),
                            flujo.readUTF(),
                            flujo.readUTF(),
                            flujo.readUTF(),
                            flujo.readUTF()
                    )
                );
            }
            this.totalQuestion = this.objList.size();
            indexQuestion = this.getNext();  
            this.setUI();
            PreguntasRandom.cerrar();              
        } catch (IOException ex) {
            
           
        }
    
    
    }
